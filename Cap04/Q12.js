/*12. Escreva um programa que peça ao usuário para digitar sua altura em metros e converta-
-a para número de ponto flutuante usando parseFloat(). Em seguida, exiba a altura
convertida no console.*/

let alturaString = prompt("Digite sua altura em metros:");
let altura = parseFloat(alturaString);

console.log(`Sua altura em metros é: ${altura}`)
