/*5. Qual é a diferença entre as palavras-chave var, let e const em JavaScript? 

Var: Evite usar var em JavaScript moderno. É considerado uma prática antiga e pode levar a problemas de escopo.

Let: Use let para variáveis cujo valor pode mudar ao longo do tempo.

Const: Use const para valores que não devem mudar, como constantes matemáticas, configurações ou elementos da interface do usuário que não precisam ser atualizados.*/